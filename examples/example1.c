#include <stdio.h>
#include <string.h>

#include "amjson.h"

/* -------------------------------------------------------------------- */
/* -------------------------------------------------------------------- */
int main(int argc __attribute__((unused)),
	 char **argv __attribute__((unused))) {

  struct jhandle jhandle;
  char *amjson = "{ \"name\" : \"bob\" }";
  
  if (amjson_alloc(&jhandle, (void *)0, 32) == 0) {
    amjson_decode(&jhandle, amjson, strlen(amjson));
    amjson_free(&jhandle);
  }
  
  return 0;
}

